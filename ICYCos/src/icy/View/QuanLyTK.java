    private void tableTKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableTKMouseClicked
        // TODO add your handling code here:
        int i = tableTK.getSelectedRow();
        if (i >= 0) {
            DangKyDTO dk = new DangKyDTO();
            dk = busdk.dsdk.get(i);
            if(dk.TaiKhoan.equals("giamdoc") && checkQuyenQLGD()==true ){
            txtTK.setText(dk.TaiKhoan);
            txtMK.setText(dk.MatKhau);
            cbbChucVu.setSelectedItem(dk.ChuVuDK);
            cbbChucVu1.setSelectedItem(dk.MaDK);
            E_CV.setText("");
            E_MK.setText("");
            E_Ma.setText("");
            E_ReMK.setText("");
            E_TK.setText("");
            txtNhapLaiMK.setText("");
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);
            }
            else if (dk.TaiKhoan.equals("giamdoc") && checkQuyenQLGD()==false ){
            txtTK.setText(dk.TaiKhoan);
            txtMK.setText(dk.MatKhau);
            cbbChucVu.setSelectedItem(dk.ChuVuDK);
            cbbChucVu1.setSelectedItem(dk.MaDK);
            E_CV.setText("");
            E_MK.setText("");
            E_Ma.setText("");
            E_ReMK.setText("");
            E_TK.setText("");
            txtNhapLaiMK.setText("");
            btnSua.setEnabled(false);
            btnXoa.setEnabled(false);
            }
            else if(!dk.TaiKhoan.equals("giamdoc")){
            txtTK.setText(dk.TaiKhoan);
            txtMK.setText(dk.MatKhau);
            cbbChucVu.setSelectedItem(dk.ChuVuDK);
            cbbChucVu1.setSelectedItem(dk.MaDK);
            E_CV.setText("");
            E_MK.setText("");
            E_Ma.setText("");
            E_ReMK.setText("");
            E_TK.setText("");
            txtNhapLaiMK.setText("");
            btnSua.setEnabled(true);
            btnXoa.setEnabled(true);

            }
               }
    }//GEN-LAST:event_tableTKMouseClicked

