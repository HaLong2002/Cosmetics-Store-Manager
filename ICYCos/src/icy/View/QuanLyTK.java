    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

           String x = null;
        for (QLNV_DTO nv : bus.dsnv) {
            if (cbbChucVu1.getSelectedItem().toString().equals(nv.Ma)) {
                x = nv.ChucVu;
            }
        }
        Pattern uname = Pattern.compile("^[a-zA-Z0-9]{3,20}$");
        Pattern pass = Pattern.compile("^[a-zA-Z0-9]{3,20}$"); //8
        if (busdk.ID(cbbChucVu1.getSelectedItem().toString()) == true) {
            E_Ma.setText("Nhân viên này đã có tài khoản");
        } else if (busdk.checkTK(txtTK.getText()) == false) {
            E_TK.setText("Tên tài khoản đã tồn tại");
            E_Ma.setText("");
        } else if (!uname.matcher(txtTK.getText()).find()) {
            E_TK.setText("Tên tài khoản phải từ 3 đến 20 ký tự");
            E_Ma.setText("");
        } else if (!pass.matcher(txtMK.getText()).find()) {
            E_MK.setVisible(true);
            E_MK.setText("Mật khẩu phải từ 3 đến 20 ký tự");
            E_TK.setText("");
            E_Ma.setText("");
        } else if (txtMK.getText().equals(txtNhapLaiMK.getText()) == false) {
            E_ReMK.setVisible(true);
            E_MK.setText("");
            E_TK.setText("");
            E_Ma.setText("");
            E_ReMK.setText("Mật khẩu không hợp lệ");
        } else if (!cbbChucVu.getSelectedItem().toString().equals(x)) {
            E_CV.setVisible(true);
            E_CV.setText("Chức vụ không hợp lệ");
            E_ReMK.setText("");
            E_MK.setText("");
            E_TK.setText("");
            E_Ma.setText("");
        }
        else {
            DangKyDTO dk = new DangKyDTO();
            dk.ChuVuDK = cbbChucVu.getSelectedItem().toString();
            dk.MaDK = cbbChucVu1.getSelectedItem().toString();
            dk.TaiKhoan = txtTK.getText();
            dk.MatKhau = txtMK.getText();
            busdk.themTK(dk);
                    Vector header = new Vector();
                    header.add("Tên Tài Khoản");
                    header.add("Mật khẩu");
                    header.add("Mã nhân viên");
                    header.add("Chức vụ");
                    if (model.getRowCount() == 0) {
                        model = new DefaultTableModel(header, 0);
                    }
                    Vector row = new Vector();
                    row.add(dk.TaiKhoan);
                    row.add(dk.MatKhau);
                    row.add(dk.MaDK);
                    row.add(dk.ChuVuDK);
                    model.addRow(row);
                   tableTK.setModel(model);
            JOptionPane.showConfirmDialog(this, "Thêm thành công");
    }
    
        

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

        int i = tableTK.getSelectedRow();
        if (i >= 0) {
            DangKyDTO dk = new DangKyDTO();
            dk.MaDK = cbbChucVu1.getSelectedItem().toString();
            dk.TaiKhoan = txtTK.getText();
            dk.MatKhau = txtMK.getText();
            dk.ChuVuDK = cbbChucVu.getSelectedItem().toString();
            if (!txtMK.getText().equals(txtNhapLaiMK.getText())) {
                E_ReMK.setText("Mật khẩu không hợp lệ");
            } else {
                busdk.sua(dk);
                E_ReMK.setText("");
                JOptionPane.showConfirmDialog(null, "Sửa thành công");
                DangKyDTO old = busdk.dsdk.set(i, dk);
                model.setValueAt(dk.TaiKhoan, i, 0);
                model.setValueAt(dk.MatKhau, i, 1);
                model.setValueAt(dk.MaDK, i, 2);
                model.setValueAt(dk.ChuVuDK, i, 3);
                tableTK.setModel(model);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn tài khoản cần sửa!!!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

